package com.openedit.hittracker;

public class SelectedFacet
{

	protected String fieldId;
	protected String[] values;
	
	public String getId()
	{
		return fieldId;
	}
	public void setId(String inId)
	{
		fieldId = inId;
	}
	public String[] getValues()
	{
		return values;
	}
	public void setValues(String[] inValues)
	{
		values = inValues;
	}
	
	
	
}
