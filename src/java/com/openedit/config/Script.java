package com.openedit.config;

public class Script
{
	protected String fieldSrc;
	protected String fieldId;
	public String getSrc()
	{
		return fieldSrc;
	}
	public void setSrc(String inSrc)
	{
		fieldSrc = inSrc;
	}
	public String getId()
	{
		return fieldId;
	}
	public void setId(String inId)
	{
		fieldId = inId;
	}
}
